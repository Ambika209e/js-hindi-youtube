// while loop

let index = 0
while (index <= 10) {
     console.log(`value of index is ${index}`);
     index = index + 2;
}

let myArr = ["apple","banana","mango"];
let i = 0;
while(i<myArr.length){
    console.log(`value is ${myArr[i]}`)
    i++;
}

// do-while loop

let score = 11
do {
    console.log(`Score is ${score}`)
    score++
} while (score <= 10);